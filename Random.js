



function setup() {
  cnv = createCanvas(windowWidth, windowHeight);
  cnv.parent("p5R");
  strokeWeight(8);


}


function draw() {
  background(204);
  fill(random(255), random(255), random(255));
  ellipse(mouseX, mouseY, pmouseX, pmouseY);
}


function mousePressed() {

if (ellipse = mousePressed){
  rect(mouseX, mouseY, pmouseX, pmouseY);


}
}
